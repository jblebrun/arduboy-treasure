// clang-format on
Game::Game(Renderer &r) : renderer(r) {}

uint8_t Game::tileAt(uint16_t x, uint16_t y) {
  // Return the tile correspond to the upper-left part of player
  uint8_t tx = ((x + mo.x) >> 3) % MAP_W;
  uint8_t ty = ((y + mo.y) >> 3) % MAP_H;
  return pgm_read_byte(&m[level][ty][tx]);
}

bool Game::checkCollisions(uint8_t target) {
  return (tileAt(po.x, po.y) == target) || (tileAt(po.x, po.y + 7) == target) ||
         (tileAt(po.x + 7, po.y) == target) ||
         (tileAt(po.x + 7, po.y + 7) == target);
}

void Game::Up() {
  if (state != PLAY) {
    return;
  }

  if (po.y <= (SCROLLS[level] ? PY_MIN : 0)) {
    mo.y--;
  } else {
    po.y--;
  }
  check();
}

void Game::Down() {
  if (state != PLAY) {
    return;
  }

  if (po.y >= (SCROLLS[level] ? PY_MAX : HEIGHT)) {
    mo.y++;
  } else {
    po.y++;
  }
  check();
}

void Game::Left() {
  if (state != PLAY) {
    return;
  }

  if (po.x <= (SCROLLS[level] ? PX_MIN : 0)) {
    mo.x--;
  } else {
    po.x--;
  }
  check();
}

void Game::Right() {
  if (state != PLAY) {
    return;
  }

  if (po.x >= (SCROLLS[level] ? PX_MAX : WIDTH)) {
    mo.x++;
  } else {
    po.x++;
  }
  check();
}

void Game::B() {
  if(state == WIN) {
    renderer.PlaySong(1, 10, true);
    state = TITLE;
    return;
  }
  renderer.PlaySong(0, 0);
  state = PLAY;
  mo = (struct offset){};
  po = (struct offset){.x = 64 - 4, .y = 32 - 4};
}

void Game::check() {
  if (stepCounter == 0) {
    renderer.Sound(55, 75, 1);
    stepCounter = 4;
  }
  stepCounter--;

  if (checkCollisions(2)) {
    renderer.Sound(110, 130, 15);
    state = DEAD;
    level = 0;
  } else if (checkCollisions(4)) {
    level++;
    mo = (struct offset){};
    po = (struct offset){.x = 64 - 4, .y = 32 - 4};
    if (level == LEVELS) {
      renderer.PlaySong(1, 5, true);
      state = WIN;
      level = 0;
    } else {
      renderer.Sound(440, 680, 15);
      state = TITLE;
    }
  }
}

void Game::drawPlayer() { renderer.DrawBitmap(po.x, po.y, sprites[3]); }

void Game::drawMap() {
  int16_t txs = mo.x >> 3;
  int16_t tys = mo.y >> 3;
  uint8_t pox = mo.x & 0x7;
  uint8_t poy = mo.y & 0x7;

  for (int16_t iy = 0; iy < 10; iy++) {
    for (int16_t ix = 0; ix < 18; ix++) {
      int16_t tx = (txs + ix) % MAP_W;
      int16_t ty = (tys + iy) % MAP_H;
      uint8_t s = pgm_read_byte(&m[level][ty][tx]);
      renderer.DrawBitmap(ix * 8 - pox, iy * 8 - poy, sprites[s]);
    }
  }
}

void Game::Render() {
  renderer.Clear();
  switch (state) {
  case TITLE:
    if (level == 0) {
      renderer.Print("FIND THE TREASURES");
    } else {
      renderer.Print("NEXT LEVEL");
    }
    break;
  case DEAD:
    renderer.Print("YOU DIED");
    break;
  case WIN:
    renderer.Print("YOU DID IT!");
    break;
  default:
    drawMap();
    drawPlayer();
  }
  renderer.Display();
}
